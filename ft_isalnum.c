#include "libft.h"

int	ft_isalnum(int ch)
{
	if (('0' <= ch && ch <= '9') ||
		('a' <= ch && ch <= 'z') ||
		('A' <= ch && ch <= 'Z'))
		return (1);
	else
		return (0);
}
